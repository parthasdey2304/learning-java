public class escape_sequence
{
	public static void main(String[] args)
	{
		// this is an escape sequence
		System.out.print("Hello!\n\n"); // this is a newline character
		System.out.println("Newlinee\b\b\b"); // This is a backspace
		System.out.println("There is a \t\t tab in between"); //  this is a tab character
		System.out.println("It ends here");
	}
}
