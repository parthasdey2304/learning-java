// This is the structure of all the data types

public class variable_data_types
{
	public static void main(String[] args)
	{
		byte b = 1; // byte : 1 byte
		short s = 23; // short : 2 byte
		char c = 'a'; // char : 2 byte
		int i = 325; // int : 4 bytes
		long l = 2343324; // long : 8 bytes
		double d = 12.42d; // double : 8 bytes
		float f = 123.3f; // float : 4 bytes

		// using all these variables in print statements
		System.out.println("Byte : " + b);
		System.out.println("Short : " + s);
		System.out.println("Char : " + c);
		System.out.println("Int : " + i);
		System.out.println("Long : " + l);
		System.out.println("Double : " + d);
		System.out.println("Float : " + f);
		
	}
}
